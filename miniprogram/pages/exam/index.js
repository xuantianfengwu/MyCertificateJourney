  // 更新考试列表，添加推荐等级
  updateExamListWithRecommendations: function() {
    const examList = this.data.examList;
    const recommendations = this.data.recommendations;
    
    const updatedExamList = examList.map(exam => {
      const examId = exam.id.toString();
      const recommendation = recommendations.find(r => r.examId === examId);
      
      let recommendLevel = 'unknown';
      let recommendLevelText = '未知';
      
      if (recommendation) {
        recommendLevel = recommendation.level;
        recommendLevelText = this.getRecommendLevelText(recommendation.level);
      }
      
      return {
        ...exam,
        recommendLevel,
        recommendLevelText
      };
    });
    
    this.setData({
      examList: updatedExamList
    });
    
    // 重新初始化分页
    this.initPagination();
  },
  
