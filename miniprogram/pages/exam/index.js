  // 解析CSV文件
  parseCSV: function(csvContent) {
    const lines = csvContent.split('\n');
    const headers = lines[0].split(',');
    const examList = [];
    
    for (let i = 1; i < lines.length; i++) {
      const values = lines[i].split(',');
      if (values.length >= headers.length) {
        const exam = {};
        for (let j = 0; j < headers.length; j++) {
          exam[headers[j]] = values[j];
        }
        
        const examId = parseInt(exam._id);
        
        // 转换数据格式
        const examItem = {
          id: examId,
          name: exam.name,
          category: exam.category || "其他",
          official_category: exam.official_category || "其他",
          official_sub_category: exam.official_sub_category || "其他",
          tags: [],
          passRate: exam.avgPassRate || "未知",
          examFee: exam.minExamFee || "未知",
          description: exam.description || "暂无描述",
          applyStatus: this.getRandomApplyStatus(),
          fitLevel: "unknown",
          idxExamFeeAsc: this.sortIndices.examFeeAsc[examId] || 999,
          idxPassRateDesc: this.sortIndices.passRateDesc[examId] || 999
        };
        
        // 添加可抵扣个税标签
        if (exam.isTaxDeduction === "1") {
          examItem.tags.push("可抵扣个税");
        }
        
        // 添加等级标签
        if (exam.examLevels && exam.examLevels !== "无") {
          examItem.tags.push("分等级");
        } else {
          examItem.tags.push("无等级");
        }
        
        examList.push(examItem);
      }
    }
    
    return examList;
  },
  
  // 构建国家职业资格名录
  buildNationalDirectory: function(examList) {
    const directoryMap = {};
    
    examList.forEach(item => {
      const category = item.official_category || "其他";
      const subCategory = item.official_sub_category || "其他";
      const examName = item.name;
      
      if (!directoryMap[category]) {
        directoryMap[category] = {};
      }
      if (!directoryMap[category][subCategory]) {
        directoryMap[category][subCategory] = [];
      }
      directoryMap[category][subCategory].push(examName);
    });
    
    // 转换为数组格式
    const nationalDirectory = Object.keys(directoryMap).map(category => {
      return {
        category: category,
        subCategories: Object.keys(directoryMap[category]).map(subCategory => {
          return {
            subCategory: subCategory,
            exams: directoryMap[category][subCategory]
          };
        })
      };
    });
    
    return nationalDirectory;
  },
  
